package com.company;

import java.util.Scanner;

public class ForEachLoop {
    public static void main(String[] args) {
        String[] fruits = {"Mango", "Apple", "Orange", "Jackfruit", "Dragon Fruits"};

        // For Each Loop
        for (String keyPoint : fruits) {
            System.out.println(keyPoint);
        }

    }
}
